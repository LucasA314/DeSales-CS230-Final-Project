class DungeonRoom():
    def __init__(self, main, w, h, d):
        self.room_width = w
        self.room_height = h
        self.difficulty = d
        self.tiles = {}
        self.movement = {}
        self.num_enemies = 0
        self.exit_exists = False
        
        for r in range(self.room_height):
            self.tiles[r] = {}
            self.movement[r] = {}
            
            for c in range(self.room_width):
                self.tiles[r][c] = 0
                self.movement[r][c] = 0

        self.generate_room(main, self.difficulty)

        #Add Intro Text
        messageToPrint = [[
									"Floor " + str(self.difficulty),
									"Good Luck!",
								 ]]

        scr_add_event_to_queue(main, EVENTS_LIST.printText.value, [messageToPrint])
                
    def set_tile(self, pos_x, pos_y, val, mval):
        self.tiles[pos_y][pos_x] = val
        self.movement[pos_y][pos_x] = mval
        
        
    def set_tiles(self, tile_map, movement_map):
        self.tiles = tile_map
        self.movement = movement_map
    
    def generate_room(self, main, difficulty_level):
        
        #Create A Room
        self.tiles = select_room_template()

        #Place the Room Start
        self.create_start()

        #Place Enemies
        self.create_enemies(main, min(difficulty_level, 10))

        #Setup Movement
        for r in range(self.room_height):
            for c in range(self.room_width):
                if (self.tiles[r][c] == 1):
                    self.set_tile(c, r, 1, 1)

        
    #Find and Place a Start Position
    def create_start(self):
        start_placed = False

        while (not start_placed):
            potential_tile = [random.randint(1, self.room_width - 3), random.randint(1, self.room_height - 3)]

            if (self.tiles[potential_tile[1]][potential_tile[0]] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 0][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 0] == 0):
                
                
                self.set_tile(potential_tile[0], potential_tile[1], 3, 0)
                start_placed = True
    
    def create_exit(self):
        exit_placed = False

        while (not exit_placed):
            potential_tile = [random.randint(1, self.room_width - 3), random.randint(1, self.room_height - 3)]

            if (self.tiles[potential_tile[1]][potential_tile[0]] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 0][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 0] == 0):
                
                
                self.set_tile(potential_tile[0] + 0, potential_tile[1] + 0, 2, 0)
                self.set_tile(potential_tile[0] + 1, potential_tile[1] + 1, 2, 0)
                self.set_tile(potential_tile[0] + 1, potential_tile[1] + 0, 2, 0)
                self.set_tile(potential_tile[0] + 0, potential_tile[1] + 1, 2, 0)
                exit_placed = True

        self.exit_exists = True

    def create_enemies(self, main, enemy_count):
        enemies_left = enemy_count

        while (enemies_left > 0):
            potential_tile = [random.randint(1, self.room_width - 3), random.randint(1, self.room_height - 3)]

            if (self.tiles[potential_tile[1]][potential_tile[0]] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 0][potential_tile[0] + 1] == 0
                and self.tiles[potential_tile[1] + 1][potential_tile[0] + 0] == 0):
                
                
                self.set_tile(potential_tile[0] + 0, potential_tile[1] + 0, 4, 0)
                self.set_tile(potential_tile[0] + 1, potential_tile[1] + 1, 4, 0)
                self.set_tile(potential_tile[0] + 1, potential_tile[1] + 0, 4, 0)
                self.set_tile(potential_tile[0] + 0, potential_tile[1] + 1, 4, 0)

                generate_enemy(main, potential_tile[0] * TILE_SIZE, potential_tile[1] * TILE_SIZE)

                self.num_enemies += 1
                enemies_left -= 1

        #Remove Special Tiles
        for r in range(self.room_height):
            for c in range(self.room_width):
                if (self.tiles[r][c] == 4):
                    self.set_tile(c, r, 0, 0)

                


